package CursoJavaLeonardoLeitao.Fundamentos.Operadores;

public class OperadoresAtribuição {

    public static void main(String[] args) {

        int a = 3;
        int b = a;
        int c = a + b;

        // c = c + b;
        c += b;

        // c = c - b;
        c -= b;

        // c = c * b;
        c *= b;

        // c = c / a;
        c /= a;

        // c = c % 2;
        c %= 2;


    }
}
